function showPreviousDate() {
    var previousDate = document.getElementById("previousDate");
    previousDate.style.display = previousDate.style.display === "none" || previousDate.style.display === "" ? "block" : "none";
  }
  
  function showNextDate() {
    var nextDate = document.getElementById("nextDate");
    nextDate.style.display = nextDate.style.display === "none" || nextDate.style.display === "" ? "block" : "none";
  }
  